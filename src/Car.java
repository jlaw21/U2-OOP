public class Car {

    //Class Attributes

    private String make;
    private String model;


    //Class Behaviors

    //returns the make attribute
    public String getMake() {
        return make;
    }

    //sets the value of the make attribute
    public void setMake(String make) {
        this.make = make;
    }

    //returns the model attribute
    public String getModel() {
        return model;
    }

    //sets the value of the model attribute
    public void setModel(String model) {
        this.model = model;
    }
}
